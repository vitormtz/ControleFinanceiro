    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        if (jComboBox1.getSelectedIndex() != 0
                && (jRadioButton1.isSelected() || (jRadioButton2.isSelected() && jComboBox2.getSelectedIndex() != 0))
                && !jTextField2.getText().isEmpty()
                && !jTextField4.getText().isEmpty()) {
            if (jRadioButton1.isSelected()) {

                Receita receita = new Receita();

                receita.setId(0);
                if (jComboBox1.getSelectedIndex() == 1) {
                    receita.setPessoaId(Integer.valueOf(System.getProperty("Login")));
                    receita.setIntegranteId(null);
                } else {
                    receita.setPessoaId(null);
                    ComboItem cpes = (ComboItem) jComboBox1.getSelectedItem();
                    receita.setIntegranteId(cpes.getCodigo());
                }
                receita.setValor(Double.parseDouble(jTextField4.getText().replace(',', '.')));
                receita.setDescricao(jTextField2.getText());

                if (new ReceitaDAO().salvar(receita)) {

                    JOptionPane.showMessageDialog(null, "Registro salvo com sucesso!");

                    jRadioButton1.setSelected(true);
                    jLabel7.setText("* Receita:");
                    jTextField2.setText("");
                    jLabel5.setVisible(false);
                    jComboBox2.setVisible(false);
                    jComboBox2.setSelectedIndex(0);
                    jTextField4.setText("");
                    jComboBox1.requestFocus();
                    new PessoaDAO().popularTabela(jTable1, jTextField3.getText());

                    for (int i = 0; i < jTable1.getRowCount(); i++) {
                        valor += Double.parseDouble(String.valueOf(jTable1.getValueAt(i, 3)).replace("R$ ", "").replace(",", "."));
                    }
                    jTextField5.setText("R$ " + Formatacao.formatarDecimal(valor));
                    valor = 0;
                } else {
                    JOptionPane.showMessageDialog(null, "Problemas ao salvar o registro.");
                }
            } else {

                Despesa despesa = new Despesa();

                despesa.setId(0);
                if (jComboBox1.getSelectedIndex() == 1) {
                    despesa.setPessoaId(Integer.valueOf(System.getProperty("Login")));
                    despesa.setIntegranteId(null);
                } else {
                    despesa.setPessoaId(null);
                    ComboItem cpes = (ComboItem) jComboBox1.getSelectedItem();
                    despesa.setIntegranteId(cpes.getCodigo());
                }
                ComboItem cdes = (ComboItem) jComboBox2.getSelectedItem();
                despesa.setTipoDespesaId(cdes.getCodigo());
                despesa.setValor(Double.parseDouble(jTextField4.getText().replace(',', '.')));
                despesa.setDescricao(jTextField2.getText());

                if (new DespesaDAO().salvar(despesa)) {

                    JOptionPane.showMessageDialog(null, "Registro salvo com sucesso!");

                    jRadioButton1.setSelected(true);
                    jLabel7.setText("* Receita:");
                    jTextField2.setText("");
                    jLabel5.setVisible(false);
                    jComboBox2.setVisible(false);
                    jComboBox2.setSelectedIndex(0);
                    jTextField4.setText("");

                    new PessoaDAO().popularTabela(jTable1, jTextField3.getText());

                    for (int i = 0; i < jTable1.getRowCount(); i++) {
                        valor += Double.parseDouble(String.valueOf(jTable1.getValueAt(i, 3)).replace("R$ ", "").replace(",", "."));
                    }
                    jTextField5.setText("R$ " + Formatacao.formatarDecimal(valor));
                    valor = 0;

                } else {
                    JOptionPane.showMessageDialog(null, "Problemas ao salvar o registro.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos corretamente");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

