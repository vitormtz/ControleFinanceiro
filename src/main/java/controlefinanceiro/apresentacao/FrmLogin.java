    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        if (new PessoaDAO().autenticarSenha(tfdEmail.getText().toLowerCase(), String.valueOf(pfdSenha.getPassword()))) {
            System.setProperty("Login", String.valueOf(new PessoaDAO().consultar(tfdEmail.getText().toLowerCase()).getId()));
            new FrmGerenciaFinancas().setVisible(true);
            this.dispose();
        } else {
            lblAvisoLogin.setVisible(true);
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

